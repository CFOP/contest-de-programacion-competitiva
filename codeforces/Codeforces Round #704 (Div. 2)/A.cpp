typedef long long ull;
typedef pair<ull, ull> pii;
typedef vector<ull> vi;

#define MAX 1000100
#define MOD 1000000007

ull p, a, b, c;

void solve(){
    cin>>p>>a>>b>>c;

    ull ans = min({a, b, c});

    if(!(p%a) || !(p%b) || !(p%c))
        ans = 0;

    ans = min({ans, a-p%a, b-p%b, c-p%c});

    cout<<ans<<"\n";

}


int main(){
    ios_base::sync_with_stdio(0); cin.tie(0);

    int t; cin>>t;
    while(t--){
        solve();
    }

    return 0;
}

