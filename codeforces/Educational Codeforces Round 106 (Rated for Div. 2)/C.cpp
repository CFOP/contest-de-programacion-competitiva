#define all(a) a.begin(), a.end()

typedef long long ull;
typedef pair<ull, ull> pii;
typedef vector<ull> vi;

#define MAX 1000100
#define MOD 1000000007

vector<ull> a;
vector<ull> b;
ull n, m;
string s;

void solve(){
    a.clear(); b.clear();
    cin>>n;
    for(int i=0; i<n; i++){
        ull z; cin>>z;
        a.push_back(z);
    }

    ull ans = n*a[0]+n*a[1];
    ull minA = a[0], minB = a[1];
    ull sum = a[0]+a[1];

    ull odd = 1, even = 1;

    for(ull i=2; i<n; i++){
        if(i%2){
            odd++;
            minB = min(minB, a[i]);
        }
        else{
            even++;
            minA = min(minA, a[i]);
        }

        sum += a[i];

        ans = min(ans, sum + minA*(n-even) + minB*(n-odd));


    }

    cout<<ans<<"\n";

}


int main(){
    ios_base::sync_with_stdio(0); cin.tie(0);
    //srand (time(NULL));

    int t=1; cin>>t;
    while(t--){
        solve();
    }

    return 0;
}

