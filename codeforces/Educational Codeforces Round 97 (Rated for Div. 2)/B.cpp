ull n;

void solve(){
    cin>>n;
    string s; cin>>s;

    ull zero(0), one(0);
    for(int i=1; i<n; i++){
        if(s[i]=='1' && s[i]==s[i-1])
            one++;
        if(s[i]=='0' && s[i]==s[i-1])
            zero++;
    }

    ull ans = 0;

    while(zero&&one){
        ans++;
        zero--;
        one--;
    }

    if(zero || one)
        ans++;

    cout<<ans<<"\n";

}


int main(){
    ios_base::sync_with_stdio(0); cin.tie(0);

    int t; cin>>t;
    while(t--){
        solve();
    }

    return 0;
}

