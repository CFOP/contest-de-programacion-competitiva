void solve(){

    cin>>n>>m;
    string s; cin>>s;

    TreeSum stSum(n+1);
    TreeMin stMin(n+1);
    TreeMax stMax(n+1);

    ull sum = 0;
    stSum.update(0, 0);
    stMin.update(0, 0);
    stMax.update(0, 0);

    for(int i=0; i<n; i++){
        if(s[i]=='+')
            sum++;
        if(s[i]=='-')
            sum--;

        stSum.update(i+1, ((s[i]=='-')?-1:1));
        stMin.update(i+1, sum);
        stMax.update(i+1, sum);
    }

    while(m--){
        int l, r; cin>>l>>r;

        ull ans = 1;
        ull sum = stSum.query(l, r+1);

        ull minValue = stMin.query(0, l);
        minValue = min(minValue, stMin.query(r+1, n+1)-sum);
        if(minValue<0)
            ans += abs(minValue);

        ull maxValue = stMax.query(0, l);
        maxValue = max(maxValue, stMax.query(r+1, n+1)-sum);
        ans += maxValue;

        cout<<ans<<"\n";
    }

}


int main(){
    ios_base::sync_with_stdio(0); cin.tie(0);

    int t; cin>>t;
    while(t--){
        solve();
    }

    return 0;
}

