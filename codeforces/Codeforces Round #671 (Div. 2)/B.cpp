typedef long long ull;
typedef pair<ull, ull> pii;

#define MAX 1000100
#define MOD 1000000007


void solve(){
    ull x; cin>>x;

    int ans = 0;
    ull p = 1;
    while(x >= (p*(p+1))/2){
        //cout<<x<<"\n";
        x -= (p*(p+1))/2;
        p += p+1;
        ans++;
    }
    cout<<ans<<"\n";
}


